  public BufferedImage getImage(int[][] data, Color color) {
    BufferedImage image = new BufferedImage(IMGSIZEX, IMGSIZEY, BufferedImage.TYPE_INT_RGB);
    int flipY;
    for(int x = 0; x < IMGSIZEX; x++){
      for(int y = 0; y < IMGSIZEY; y++){
        int pixelColor = data[y][x];
        flipY = IMGSIZEY - y - 1;
        if(pixelColor == 0){
          pixelColor = Color.BLACK.getRGB();
        } else {
          pixelColor = color.getRGB();
        }
        image.setRGB(x, flipY, pixelColor);
      }
    }
    return image;
  }
